#include "printf.h"

int	print_char(va_list list, char c)
{
	char	s;

	if (c == 'c')
	{
		s = va_arg(list, int);
		write(1, &s, 1);
	}
	else if (c == '%')
		write(1, "%", 1);
	return (1);
}